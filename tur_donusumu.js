// string ifadeyi integer ve floata çevirme
let sayi="15";
console.log(sayi," değeri ", typeof(sayi));
//integera dönüştürme
sayi1=parseInt(sayi);
console.log(sayi1, " değeri ", typeof(sayi));

//floata dönüştürme
let sayi2=11.5;
sayi2=parseFloat(sayi2);
console.log(sayi2,"değeri",typeof(sayi2));

//integerdan stringe dönüştürme
